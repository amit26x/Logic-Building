//Using For Loop
console.log("Using for Loop");
let i = 0;
for(let i=1; i<=5; i++){
    console.log(i);
}


// Using whlie Loop
console.log("Using while Loop");
let j = 1;
while (j<=5){
    console.log(j);
    j++;
}



// Using Do-While Loop
console.log("Using Do-While Loop");
let k = 1;
do{
    console.log(k);
    k++;
}while(k<=5);



